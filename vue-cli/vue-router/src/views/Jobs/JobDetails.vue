<template>
  <h1>Jobs Details Page</h1>
  <div v-if="job">
    <h2>{{ job.title }}</h2>
    <p>Job id is {{ id }}</p>
  </div>
</template>

<script>
export default {
  props: ["id"],
  data() {
    return {
      job: null,
    };
  },
  mounted() {
    fetch("http://localhost:3000/jobs/" + this.id)
      .then((response) => response.json())
      .then((data) => (this.job = data))
      .catch((err) => console.log(err.message));
  },
};
</script>

